export function getPlantCare(name: string): PlantCareInfo | null {
  const key = Object.keys(PLANT_CARE).find(k =>
    name.toLowerCase().includes(k.toLowerCase()) || k.toLowerCase().includes(name.toLowerCase().split(' ')[0])
  );
  return key ? PLANT_CARE[key] : null;
}
